course_format2 = "Pyton 'Programming'"
course_format3 = "Pyton \\Programming\\"
course_format4 = "Pyton \nProgramming"

print("*" * 30)
print("course_format1: " + course_format1)
print("course_format2: " + course_format2)
print("course_format3: " + course_format3)
print("course_format4: " + course_format4)
