# List
letter = [1, 2, 3, 4]
print(f"letter = {letter}")
# ----------------->
martix = [[0, 1], [2, 3]]
print(f"martix = {martix}")
# ----------------->
zero = [1] * 5
print(f"zero = {zero}")
# ----------------->
combine = letter + zero
print(f"combine = {combine}")
# ------------------>
test1 = range(5)
print(f"test1 = {test1}")
print(f"test1 = {list(test1)}")
# ------------------->
test2 = "Python"
print(f"test2 = {test2}")
print(f"test2 = {list(test2)}")
print(f"test2 = {len(list(test2))}")
